// Helper functions to work with flashcards
export const getFlashcardsByCategory = (category: any): Flashcard[] => {
    return flashcards.filter((card) => card.category === category);
};

export const getFlashcardsByDifficulty = (
    difficulty: any
): Flashcard[] => {
    return flashcards.filter((card) => card.difficulty === difficulty);
};

export const getAllCategories = (): string[] => {
    return [...new Set(flashcards.map((card) => card.category))];
};

export const getRandomFlashcard = (): Flashcard => {
    const randomIndex = Math.floor(Math.random() * flashcards.length);
    return flashcards[randomIndex];
};

export const shuffleFlashcards = (cards: Flashcard[]): Flashcard[] => {
    const shuffled = [...cards];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
};
 